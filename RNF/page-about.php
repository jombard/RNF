	            <h3>Remembering Not to Forget Constitution</h3>
        
	            <ol>
		            <li>
                        <h4>Name</h4>
                        <p>The charity's name is Remembering Not To Forget</p>
                    </li>
			        <li>
                        <h4>The purposes of the charity are:</h4>
                        <p>Raise awareness of dementia and its effects</p>
                        <p>Raise money to support people impacted by dementia (patients, families and carers), 
                            and to fund research</p>
                    </li>
			        <li>
                        <h4>Trustees</h4>
                        <p>The charity shall be managed by a committee of trustees who are appointed at the Annual 
                            General Meeting (AGM) of the charity</p>
                    </li>
			        <li>
                        <h4>Carrying out the purposes</h4>
                        <p>In order to carry out the charitable purposes, the trustees have the power to:</p>
                        <ul class="rnfflower">
                            <li>raise funds, receive grants and donations</li>
                            <li>apply funds to carry out the work of the charity</li>
                            <li>co-operate with and support other charities with similar purposes</li>
                            <li>do anything which is lawful and necessary to achieve the purposes</li>
                        </ul>
                    </li>
			        <li>
                        <h4>Membership</h4>
                        <p>The charity shall have a membership. People who support the work of the charity and are 
                            aged 18 or over, can apply to the trustees to become a member. Once accepted by the 
                            trustees, membership lasts for 3 years and may be renewed. The trustees will keep an 
                            up-to-date membership list.</p>
                        <p>The trustees may remove a person's membership if they believe it is in the best 
                            interests of the charity. The member has the right to be heard by the trustees 
                            before the decision is made and can be accompanied by a friend.</p>
                    </li>
			        <li>
                        <h4>Annual general meeting - AGM</h4>
                        <ul class="rnfflower">
                            <li>The AGM must be held every year, with 14 days notice given to all members telling 
                                them what is on the agenda. Minutes must be kept of the AGM.</li>
                            <li>There must be at least 3 members present at the AGM.</li>
                            <li>Every member has one vote.</li>
                            <li>The trustees shall present the annual report and accounts.</li>
                            <li>Any member may stand for election as a trustee.</li>
                            <li>Members shall elect between 3 and 10 trustees to serve for the next year. 
                                They will retire at the next AGM but may stand for re-election.</li>
                        </ul>
                    </li>
			        <li>
                        <h4>Trustee meetings</h4>
                        <ul class="rnfflower">
                            <li>Trustees must hold at least 3 meetings each year. At their first meeting after the 
                                AGM they will elect a chair, treasurer and secretary. Trustees may act by majority 
                                decision.</li>
                            <li>At least 3 trustees must be present at the meeting (in person or by telephone or 
                                video conference) to be able to take decisions. Minutes shall be kept for every 
                                meeting.</li>
                            <li>If trustees have a conflict of interest they must declare it and leave the meeting 
                                while this matter is being discussed or decided.</li>
                            <li>During the year, the trustees may appoint up to 2 additional trustees. They will 
                                stand down at the next AGM.</li>
                            <li>The trustees may make reasonable additional rules to help run the charity. 
                                These rules must not conflict with this constitution or the law.</li>
                        </ul>
                    </li>
			        <li>
                        <h4>Money and property</h4>
                        <ul class="rnfflower">
                            <li>Money and property must only be used for the charity's purposes.</li>
                            <li>Trustees must keep accounts. The most recent annual accounts can be seen by 
                                anybody on request.</li>
                            <li>Trustees cannot receive any money or property from the charity, except to refund 
                                reasonable out of pocket expenses.</li>
                            <li>Money must be held in the charity's bank account. All cheques must be signed by two 
                                trustees.</li>
                            <li>Money collected by the charity will be distributed as decided by a majority vote 
                                by the Trustees at each trustee meeting. If there is any conflict or deadlock then 
                                the total funds collected at the time of the conflict/deadlock shall be divided 
                                equally between the Chair and Secretary to allocate.</li>
                        </ul>
                    </li>
			        <li>
                        <h4>General meetings</h4>
                        <p>If the Trustees consider it is necessary to change the constitution, or wind up the 
                            charity, they must call a General Meeting so that the membership can make the decision. 
                            Trustees must also call a General Meeting if they receive a written request from the 
                            majority of the members. All members must be given 14 days notice and told the reason 
                            for the meeting. All decisions require a two thirds majority. Minutes must be kept.</p>
                        <ul class="rnfflower">
                            <li>Winding up - any money or property remaining after payment of debts must be given 
                                to a charity with similar purposes to this one.</li>
                            <li>Changes to the Constitution - can be made at AGMs or General Meetings. No change 
                                can be made that would make the organisation no longer a charity.</li>
                            <li>General Meeting - called on written request from a majority of members.</li>
                            <li>Trustees may also call a General Meeting to consult the membership.</li>
                        </ul>
                    </li>
			        <li>
                        <h4>Setting up the charity</h4>
                        <p>This constitution was adopted on 1st December 2013 by the people whose signatures 
                            appear below (Laura Corrick, Alison Turnbull, Helen Turnbull, Matthew Corrick, 
                            Kathryn Simpson). They are the first members of the charity and will be the trustees 
                            until the AGM, which must be held within one year of this date.</p>
                    </li>
		        </ol>

