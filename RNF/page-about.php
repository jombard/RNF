                <h3>More about dementia</h3>

                <p>Memory makes you who you are. Dementia slowly robs you of that. Your personality, the 
                    experiences that have shaped you, that you’ve shared with the people you love, your ability 
                    to carry out everyday tasks like dress, wash and feed yourself, the words you need to communicate 
                    with people. It all slips away.</p>
			   
               <p>There are currently 800,000 people with dementia in the UK, and more than 35 million people 
                    worldwide. This number is expected to almost double every 20 years. Unfortunately this 
                    means that most people will be affected by dementia at some point in their lives, yet it 
                    is a condition that most people know very little about.</p>

                <p>We want to emphasise the five key messages shared by the 
                    <a href="https://www.dementiafriends.org.uk/" target="_blank">Dementia Friends</a> initiative:</p>

                <ul class="rnfflower">
                    <li>
                        <h4>Dementia is not a natural part of aging</h4>
                        <p>Not everyone who grows old will develop dementia, and not everyone who develops dementia 
                            is old.</p>
                        <p>One in three people over 65 will develop dementia, but it also affects 
                            <a href="http://www.alzheimers.org.uk/site/scripts/documents.php?categoryID=200350"  
                            target="_blank">younger people</a>. 
                            There are currently estimated to be over 40,000 people under 65 living with dementia 
                            in the UK, however the number is likely to be much higher as people often do not seek 
                            a diagnosis at a younger age. People as young as 30 have been diagnosed with dementia.</p>
                        <p>Raising awareness of dementia in younger people is one of our 
                            <a href="/">Remembering Not To Forget</a>’s central aims. You can read more about 
                            organisations offering support to younger people with dementia on our resources page.</p>
                    </li>
                    <li>
                        <h4>Dementia is caused by diseases of the brain</h4>
                        <p>Dementia is the name for a collection of progressive symptoms which include memory loss, 
                            mood changes and problems with communication and reasoning. These symptoms are brought 
                            about by a number of diseases that cause changes in the brain. The most common of these 
                            is Alzheimer's disease (affecting 62% of those living with dementia), which changes the 
                            chemistry and structure of the brain causing the brain cells to die.</p>
                        <p>Other types of dementia include vascular dementia and Pick's disease</p>
                        <p><a href="https://www.alzheimers.org.uk/site/scripts/documents.php?categoryID=200362" 
                            target="_blank">Read about different kinds of dementia</a>.</p>
                    </li>
                    <li>
                        <h4>Dementia is not just about losing your memory</h4>
                        <p>People often think of dementia as a form of memory loss, and usually it does start by 
                            affecting people's short-term memory. But it's more than that - it can also affect the 
                            way people think, speak, perceive things, feel and behave. Dementia makes it harder to 
                            communicate and do everyday things, but there is a lot that can be done to help. Every 
                            year we understand more about dementia, and develop new strategies that can help to boost 
                            someone's confidence and maintain their independence for as long as possible.</p>
                    </li>
                    <li>
                        <h4>It’s possible to live well with dementia</h4>
                        <p>Most of us have an image in our mind of what life with dementia looks like. That image is 
                            often very bleak. Dementia does of course bring many challenges, but with the right 
                            knowledge and support it is possible to live well with dementia for a long time.</p>
                        <p>What people can do and how long they can do it for will depend on the circumstances of 
                            each individual. People with dementia can continue to drive, socialise and hold down 
                            satisfying jobs. Even as dementia progresses, many people lead active, healthy lives, 
                            continue their hobbies, and enjoy loving friendships and relationships.</p>
                    </li>
                    <li>
                        <h4>There’s more to a person than the dementia</h4>
                        <p>Life doesn't end when dementia begins. When someone is diagnosed, their plans for the 
                            future might change and they may need more help and support to keep doing the things 
                            they enjoy. However, just as we would look at someone with cancer or diabetes and see 
                            a person first, there is more to the person than the dementia.</p>
                    </li>
                </ul> 

                <p><a href="/">Remembering Not To Forget</a> trustees have been writing about their personal 
                    experiences of dementia in a series of <a href="/blog">blogs</a>.</p>

                <p>Some of us do become more forgetful as we get older or during times of stress or illness, 
                    but dementia is a different sort of forgetfulness. With dementia the memory loss will be 
                    more noticeable, and may be accompanied by mood changes and confusion. If you’re worried 
                    about memory loss it's important to ask your GP to check out any unusual symptoms as these 
                    can sometimes be treated with appropriate medication. Check here for 
                    <a href="http://www.alzheimers.org.uk/site/scripts/documents.php?categoryID=200341" 
                    target="_blank">more information</a>. 

                <p>Government investment in dementia research has doubled since 2010, and 
                    <a href="https://www.gov.uk/government/news/pm-announces-funding-for-uks-first-dementia-research-institute" 
                    target="_blank">further commitments</a> 
                    were made at the end of 2015. This is fantastic news, however funding for dementia research, 
                    treatment and care is still far below what’s needed.</p>
                <p><a href="/">Remembering Not To Forget</a> is working to raise additional funds and to raise 
                    awareness of dementia.</p>
                <p>We need your <a href="/support">support</a>.</p>

                <p>Looking for more information about dementia? Check our handy list of 
                    <a href="/resources">resources</a>.</p>

